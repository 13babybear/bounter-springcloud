package cn.bounter.simon.model.po;

public class Bounter {

    private Long id;

    private String name;

    public Long getId() {
        return id;
    }

    public Bounter setId(Long id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public Bounter setName(String name) {
        this.name = name;
        return this;
    }
}
